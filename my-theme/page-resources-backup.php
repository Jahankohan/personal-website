document.addEventListener('DOMContentLoaded', function() {<?php

    const categoryButtons = document.querySelectorAll('[data-category]');get_footer();
    const resourceCards = document.querySelectorAll('.resource-card');
    
    categoryButtons.forEach(button => {
        button.addEventListener('click', function() {
            const category = this.getAttribute('data-category');
            
            // Update button states
            categoryButtons.forEach(btn => {
                const btnCategory = btn.getAttribute('data-category');
                if (btnCategory === category) {
                    // Active state
                    if (btnCategory === 'all') {
                        btn.style.backgroundColor = 'var(--deep-tech-blue)';
                        btn.style.borderColor = 'var(--deep-tech-blue)';
                        btn.style.color = 'white';
                    } else {
                        const color = getColorForCategory(btnCategory);
                        btn.style.backgroundColor = color;
                        btn.style.borderColor = color;
                        btn.style.color = 'white';
                    }
                } else {
                    // Inactive state
                    const color = getColorForCategory(btn.getAttribute('data-category'));
                    btn.style.backgroundColor = 'transparent';
                    btn.style.borderColor = color;
                    btn.style.color = color;
                }
            });
            
            // Filter resources
            resourceCards.forEach(card => {
                if (category === 'all' || card.getAttribute('data-category') === category) {
                    card.style.display = 'block';
                } else {
                    card.style.display = 'none';
                }
            });
        });
    });
    
    function getColorForCategory(category) {
        const colors = {
            'all': 'var(--deep-tech-blue)',
            'whitepaper': 'var(--electric-cyan)',
            'survey': 'var(--warm-ember)',
            'market-insights': 'var(--neural-purple)',
            'future-trends': 'var(--sunset-orange)'
        };
        return colors[category] || 'var(--deep-tech-blue)';
    }
});
